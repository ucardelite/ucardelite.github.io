export const checkCardName = (value) =>{
    if(value === 'm1'){
        return "Silver Brushed"
    }
    if(value === 'm2'){
        return 'Rose Gold Brushed'
    }
    if(value === 'm3'){
        return 'Matte Black'
    }
    if(value === 'm4'){
        return 'Gold Brushed'
    }
    else{
        return 'Rose Gold Brushed'
    }
}